package kr.lim.builder;

public class Person {
	private String name;
	private String schoolName;
	private int age;
	private int height;
	private int weight;
	
	Person(String name, String schoolName, int age, int height, int weight) {
		this.name = name;
		this.schoolName = schoolName;
		this.age = age;
		this.height = height;
		this.weight = weight;
		
	}
}
